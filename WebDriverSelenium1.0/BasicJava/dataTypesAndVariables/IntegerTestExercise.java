package dataTypesAndVariables;

public class IntegerTestExercise {

	public static void main(String[] args)
	{
		//Use the int keyword to declare a integer variable
				int carSpeed;
				
				// Use the integer variable with value 20
				carSpeed=20;
				
				//Print the value of the integer variable
				System.out.println("Car is running at the speed of: "+ carSpeed);
				
				//Change the value of integer variable
				
				carSpeed=carSpeed + 20;
				
				//Print the value of integer variable after changed
				System.out.println("Car is running at the speed of: "+ carSpeed);

	}

}
